package fenetre;

public class Model {
	private int cid;
	private String shemHevra;
	private String path;
	private int year1;
	private int year2;
	private String tohna;
	
	public int getCid() {
		return cid;
	}
	public void setCid(int cid) {
		this.cid = cid;
	}
	public String getShemHevra() {
		return shemHevra;
	}
	public void setShemHevra(String shemHevra) {
		this.shemHevra = shemHevra;
	}
	public String getPath() {
		return path;
	}
	public void setPath(String path) {
		this.path = path;
	}
	public int getYear1() {
		return year1;
	}
	public void setYear1(int year1) {
		this.year1 = year1;
	}
	public int getYear2() {
		return year2;
	}
	public void setYear2(int year2) {
		this.year2 = year2;
	}
	public String getTohna() {
		return tohna;
	}
	public void setTohna(String tohna) {
		this.tohna = tohna;
	}
	
	
}
